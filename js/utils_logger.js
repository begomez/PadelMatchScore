//////////////////////////////////////////////////////////////
// Utils to log both messages and errors
//////////////////////////////////////////////////////////////

/**
 * Log message in output
 */
function logMessage(msg) {
	//alert(msg);
	console.log(msg);
}

function logChange() {
	alert("Change here...");
}

/**
 * Log error in error output
 */
function logError(errorMsg) {
	//alert(errorMsg);
	console.error(errorMsg);
}